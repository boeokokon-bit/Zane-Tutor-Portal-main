// Shuffle array using Fisher-Yates algorithm
const shuffleArray = <T,>(array: T[]): T[] => {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};

export const getQuestionsForSubject = (subject: Subject, count: number = 10): Question[] => {
  const subjectQuestions = questionBank.filter(q => q.subject === subject);
  // Shuffle and take requested number
  const shuffled = shuffleArray(subjectQuestions);
  return shuffled.slice(0, count);
};

export const getQuestionsForSubjects = (subjects: Subject[], count: number = 10): Record<Subject, Question[]> => {
  const result: Partial<Record<Subject, Question[]>> = {};
  subjects.forEach(subject => {
    result[subject] = getQuestionsForSubject(subject, count);
  });
  return result as Record<Subject, Question[]>;
};

